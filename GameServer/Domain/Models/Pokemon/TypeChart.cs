        public static float GetEffectiveness(PokemonType attackType, PokemonType defenseType)
        {
            if (attackType == PokemonType.None || defenseType == PokemonType.None)
                return 1;
            int row = (int)attackType - 1;
            int col = (int)defenseType - 1;
            return chart[row][col];
        }

        public static float GetTypeEffectiveness(PokemonType attackType, Pokemon defender)
        {
            float effectiveness = GetEffectiveness(attackType, defender.PrimaryType);

            if (defender.SecondaryType.HasValue)
            {
                effectiveness *= GetEffectiveness(attackType, defender.SecondaryType.Value);
            }

            return effectiveness;
        }
